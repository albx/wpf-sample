        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            var calc = Process.Start("cmd.exe");
            calc.WaitForExit(5000);

            Thread.Sleep(5000);
            calc.Kill();
        }
